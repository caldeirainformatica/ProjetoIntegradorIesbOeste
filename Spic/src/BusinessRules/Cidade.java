public class Cidade {
	/**
	 * @param cidade
	 */
	public Cidade(String cidade) {
		super();
		this.cidade = cidade;
	}

	private String cidade;

	/**
	 * @return the cidade
	 */
	public String getCidade() {
		return cidade;
	}

	/**
	 * @param cidade the cidade to set
	 * Metodo construtor da classe Cidade
	 */
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	

}
